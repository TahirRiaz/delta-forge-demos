# Check 5: Cross-version consistency (if multiple metadata versions exist)
# ---------------------------------------------------------------------------
def check_version_progression(table_path, verbose=False):
    print(f"\n{BOLD}Check 5: Metadata version progression{RESET}")

    meta_dir = os.path.join(table_path, "metadata")
    meta_files = sorted(glob.glob(os.path.join(meta_dir, "v*.metadata.json")))
    gz_files = sorted(glob.glob(os.path.join(meta_dir, "v*.metadata.json.gz")))
    all_meta = sorted(meta_files + gz_files)

    if len(all_meta) < 2:
        info("Only one metadata version — progression check skipped")
        return

    prev_snap_count = 0
    for meta_path in all_meta:
        try:
            if meta_path.endswith(".gz"):
                with gzip.open(meta_path, "rt") as f:
                    m = json.load(f)
            else:
                with open(meta_path) as f:
                    m = json.load(f)

            snap_count = len(m.get("snapshots", []))
            version_name = os.path.basename(meta_path)

            if snap_count >= prev_snap_count:
                ok(f"{version_name}: {snap_count} snapshot(s) "
                   f"(monotonically increasing)")
            else:
                fail(f"{version_name}: {snap_count} snapshots < "
                     f"previous {prev_snap_count} (regression!)")

            prev_snap_count = snap_count

            if verbose:
                snaps = m.get("snapshots", [])
                for s in snaps:
                    sid = s.get("snapshot-id", "?")
                    records = s.get("summary", {}).get("total-records", "?")
                    info(f"    snapshot {sid}: {records} records")

        except Exception as e:
            fail(f"Could not parse {os.path.basename(meta_path)}: {e}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------
def main():
    parser = argparse.ArgumentParser(
        description="Verify Iceberg metadata generated by Delta Forge UniForm"
    )
    parser.add_argument(
        "table_path",
        help="Path to the Delta table root (must contain metadata/ directory)"
    )
    parser.add_argument(
        "--verbose", "-v",
        action="store_true",
        help="Show detailed output (schemas, sample data, manifest entries)"
    )
    parser.add_argument(
        "--format-version", "-f",
        type=int,
        choices=[1, 2, 3],
        default=None,
        help="Assert the table uses this Iceberg format version (1, 2, or 3)"
    )
    args = parser.parse_args()

    table_path = os.path.abspath(args.table_path)

    print(f"{BOLD}{CYAN}{'='*60}{RESET}")
    print(f"{BOLD}{CYAN}  Iceberg UniForm Metadata Verifier{RESET}")
    print(f"{BOLD}{CYAN}{'='*60}{RESET}")
    print(f"  Table: {table_path}")

    if not os.path.isdir(table_path):
        print(f"\n{RED}Error: {table_path} is not a directory{RESET}")
        sys.exit(1)

    # Check for Delta log to confirm this is a Delta table
    delta_log = os.path.join(table_path, "_delta_log")
    if os.path.isdir(delta_log):
        info(f"Delta log found — this is a Delta table with UniForm")
    else:
        warn("No _delta_log/ directory — this may be a pure Iceberg table")

    # Run checks
    latest_meta = check_metadata_dir(table_path)
    if latest_meta is None:
        print(f"\n{RED}ABORT: Cannot proceed without metadata files{RESET}")
        sys.exit(1)

    metadata = check_metadata_json(latest_meta, verbose=args.verbose,
                                    expected_version=args.format_version)
    if metadata is None:
        print(f"\n{RED}ABORT: Cannot proceed without valid metadata.json{RESET}")
        sys.exit(1)

    check_manifest_chain(table_path, metadata, verbose=args.verbose)
    check_pyiceberg_scan(table_path, metadata, verbose=args.verbose)
    check_version_progression(table_path, verbose=args.verbose)

    # Summary
    print(f"\n{BOLD}{'='*60}{RESET}")
    print(f"{BOLD}  SUMMARY{RESET}")
    print(f"{'='*60}")
    print(f"  {GREEN}\u2713 Passed: {passed}{RESET}")
    if failed:
        print(f"  {RED}\u2717 Failed: {failed}{RESET}")
    if warned:
        print(f"  {YELLOW}\u26a0 Warnings: {warned}{RESET}")

    if failed == 0:
        print(f"\n  {GREEN}{BOLD}Iceberg metadata is valid and consistent!{RESET}")
        print(f"  Any Iceberg-compatible engine can read this table.")
    else:
        print(f"\n  {RED}{BOLD}Iceberg metadata has issues — see failures above.{RESET}")

    sys.exit(1 if failed > 0 else 0)


