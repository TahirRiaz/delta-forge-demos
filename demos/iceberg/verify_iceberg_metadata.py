#!/usr/bin/env python3
"""
Iceberg UniForm Metadata Verifier
==================================
Validates that Iceberg metadata generated by Delta Forge's UniForm hook is
structurally correct and data-consistent with the underlying Parquet files.

This script does NOT read the Delta transaction log at all. It reads ONLY
through the Iceberg metadata chain, proving that an external Iceberg engine
(Spark, Trino, DuckDB) would see the correct data.

Usage:
    python3 verify_iceberg_metadata.py <table_path> [--verbose]

The table_path should point to the root of a Delta table that has UniForm
enabled (i.e. it has both _delta_log/ and metadata/ directories).

Checks performed:
  1. metadata/ directory exists with v{N}.metadata.json
  2. metadata.json is valid JSON with required Iceberg fields
  3. Schema has correct field count and types
  4. Snapshot chain is valid (each snapshot references a manifest list)
  5. Manifest list Avro is parseable and references valid manifest files
  6. Manifest Avro is parseable and references existing Parquet data files
  7. PySpark reads the data through Iceberg format and returns expected row count
  8. Row count and column names from PySpark match metadata claims

Requirements:
    pip install pyiceberg[pyarrow] fastavro
"""

import argparse
import glob
import gzip
import json
import os
import sys
from pathlib import Path

# ---------------------------------------------------------------------------
# ANSI colours for terminal output
# ---------------------------------------------------------------------------
GREEN = "\033[92m"
RED = "\033[91m"
YELLOW = "\033[93m"
CYAN = "\033[96m"
BOLD = "\033[1m"
RESET = "\033[0m"

passed = 0
failed = 0
warned = 0


def ok(msg):
    global passed
    passed += 1
    print(f"  {GREEN}\u2713{RESET} {msg}")


def fail(msg):
    global failed
    failed += 1
    print(f"  {RED}\u2717{RESET} {msg}")


def warn(msg):
    global warned
    warned += 1
    print(f"  {YELLOW}\u26a0{RESET} {msg}")


def info(msg):
    print(f"  {CYAN}\u2022{RESET} {msg}")


# ---------------------------------------------------------------------------
# Check 1: metadata/ directory structure
# ---------------------------------------------------------------------------
def check_metadata_dir(table_path):
    print(f"\n{BOLD}Check 1: Metadata directory structure{RESET}")
    meta_dir = os.path.join(table_path, "metadata")
    if not os.path.isdir(meta_dir):
        fail(f"metadata/ directory not found at {meta_dir}")
        return None

    ok("metadata/ directory exists")

    # Find version-hint.text
    hint_path = os.path.join(meta_dir, "version-hint.text")
    if os.path.isfile(hint_path):
        with open(hint_path) as f:
            hint = f.read().strip()
        ok(f"version-hint.text exists (points to version {hint})")
    else:
        warn("version-hint.text not found (non-authoritative, not fatal)")

    # Find metadata JSON files
    meta_files = sorted(glob.glob(os.path.join(meta_dir, "v*.metadata.json")))
    gz_files = sorted(glob.glob(os.path.join(meta_dir, "v*.metadata.json.gz")))

    if not meta_files and not gz_files:
        fail("No v{N}.metadata.json files found")
        return None

    all_meta = meta_files + gz_files
    latest = all_meta[-1]
    version_count = len(all_meta)
    ok(f"Found {version_count} metadata version(s), latest: {os.path.basename(latest)}")

    # Find Avro files
    avro_files = glob.glob(os.path.join(meta_dir, "*.avro"))
    if avro_files:
        manifest_lists = [f for f in avro_files if "snap-" in os.path.basename(f)]
        manifests = [f for f in avro_files if "snap-" not in os.path.basename(f)]
        ok(f"Found {len(manifest_lists)} manifest list(s) and {len(manifests)} manifest(s)")
    else:
        warn("No Avro files found in metadata/ (may be inline or first commit pending)")

    return latest


# ---------------------------------------------------------------------------
# Check 2: Parse and validate metadata.json
# ---------------------------------------------------------------------------
def check_metadata_json(meta_path, verbose=False, expected_version=None):
    print(f"\n{BOLD}Check 2: Iceberg metadata.json validation{RESET}")

    try:
        if meta_path.endswith(".gz"):
            with gzip.open(meta_path, "rt") as f:
                metadata = json.load(f)
            ok("Gzip-compressed metadata.json parsed successfully")
        else:
            with open(meta_path) as f:
                metadata = json.load(f)
            ok("metadata.json parsed as valid JSON")
    except (json.JSONDecodeError, OSError) as e:
        fail(f"Failed to parse metadata.json: {e}")
        return None

    # Required top-level fields
    required_fields = ["format-version", "table-uuid", "location"]
    for field in required_fields:
        if field in metadata:
            ok(f"Required field '{field}' present")
        else:
            fail(f"Missing required field '{field}'")

    fmt_version = metadata.get("format-version", "?")
    info(f"Iceberg format version: {fmt_version}")

    if expected_version is not None:
        if fmt_version == expected_version:
            ok(f"Format version matches expected: {expected_version}")
        else:
            fail(f"Format version mismatch: got {fmt_version}, expected {expected_version}")

    # Schema validation
    if fmt_version == 1:
        # V1 accepts either legacy top-level 'schema' or modern 'schemas' array
        schema = metadata.get("schema")
        schemas = metadata.get("schemas", [])
        if schema:
            fields = schema.get("fields", [])
            ok(f"V1 top-level schema present with {len(fields)} field(s)")
        elif schemas:
            latest_schema = schemas[-1]
            fields = latest_schema.get("fields", [])
            ok(f"V1 schemas array has {len(schemas)} version(s), "
               f"latest schema has {len(fields)} field(s)")
        else:
            fail("V1 metadata missing both 'schema' and 'schemas'")
            fields = []
    else:
        # V2/V3 use 'schemas' array
        schemas = metadata.get("schemas", [])
        if schemas:
            latest_schema = schemas[-1]
            fields = latest_schema.get("fields", [])
            ok(f"V{fmt_version} schemas array has {len(schemas)} version(s), "
               f"latest schema has {len(fields)} field(s)")
        else:
            fail(f"V{fmt_version} metadata missing 'schemas' array")
            fields = []

    if verbose and fields:
        info("Schema fields:")
        for f in fields:
            info(f"    {f.get('name', '?')}: {f.get('type', '?')} "
                 f"(id={f.get('id', '?')}, required={f.get('required', '?')})")

    # Snapshots
    snapshots = metadata.get("snapshots", [])
    if snapshots:
        ok(f"Found {len(snapshots)} snapshot(s)")
        latest_snap = snapshots[-1]
        summary = latest_snap.get("summary", {})
        total_records = summary.get("total-records", "?")
        total_files = summary.get("total-data-files", "?")
        info(f"Latest snapshot: {total_records} records in {total_files} data file(s)")
    else:
        warn("No snapshots found (table may be empty or first commit pending)")

    # Partition spec
    if fmt_version == 1:
        partition_spec = metadata.get("partition-spec", [])
        partition_specs = metadata.get("partition-specs", [])
        if partition_spec:
            ok(f"V1 partition-spec has {len(partition_spec)} field(s)")
        elif partition_specs:
            latest_spec = partition_specs[-1]
            spec_fields = latest_spec.get("fields", [])
            if spec_fields:
                ok(f"V1 partition-specs has {len(spec_fields)} field(s)")
            else:
                info("Unpartitioned table (empty partition fields)")
        else:
            info("Unpartitioned table (no partition-spec)")
    else:
        partition_specs = metadata.get("partition-specs", [])
        if partition_specs:
            latest_spec = partition_specs[-1]
            spec_fields = latest_spec.get("fields", [])
            if spec_fields:
                ok(f"Partition spec has {len(spec_fields)} field(s): "
                   f"{[f.get('name', '?') for f in spec_fields]}")
            else:
                info("Unpartitioned table (empty partition fields)")
        else:
            info("No partition-specs array (unpartitioned)")

    # Snapshot log
    snap_log = metadata.get("snapshot-log", [])
    if snap_log:
        ok(f"Snapshot log has {len(snap_log)} entries (audit trail)")

    return metadata


