# Check 3: Parse Avro manifest chain
# ---------------------------------------------------------------------------
def check_manifest_chain(table_path, metadata, verbose=False):
    print(f"\n{BOLD}Check 3: Avro manifest chain validation{RESET}")

    try:
        import fastavro
    except ImportError:
        warn("fastavro not installed — skipping Avro manifest validation")
        warn("Install with: pip install fastavro")
        return

    snapshots = metadata.get("snapshots", [])
    if not snapshots:
        warn("No snapshots to validate")
        return

    latest_snap = snapshots[-1]
    manifest_list_path = latest_snap.get("manifest-list", "")

    if not manifest_list_path:
        warn("Latest snapshot has no manifest-list path")
        return

    # Resolve the manifest list path (may be absolute or relative)
    if not os.path.isabs(manifest_list_path):
        manifest_list_path = os.path.join(table_path, manifest_list_path)

    # Try resolving from metadata/ if not found directly
    if not os.path.isfile(manifest_list_path):
        basename = os.path.basename(manifest_list_path)
        alt_path = os.path.join(table_path, "metadata", basename)
        if os.path.isfile(alt_path):
            manifest_list_path = alt_path

    if not os.path.isfile(manifest_list_path):
        fail(f"Manifest list file not found: {manifest_list_path}")
        return

    # Parse manifest list
    try:
        with open(manifest_list_path, "rb") as f:
            manifest_list_records = list(fastavro.reader(f))
        ok(f"Manifest list Avro parsed: {len(manifest_list_records)} manifest(s) referenced")
    except Exception as e:
        fail(f"Failed to parse manifest list Avro: {e}")
        return

    total_data_files = 0
    for i, ml_record in enumerate(manifest_list_records):
        manifest_path = ml_record.get("manifest_path", "")
        if verbose:
            info(f"  Manifest {i}: {os.path.basename(manifest_path)}")
            added = ml_record.get("added_data_files_count", "?")
            existing = ml_record.get("existing_data_files_count", "?")
            info(f"    added_data_files={added}, existing_data_files={existing}")

        # Resolve manifest path
        if not os.path.isabs(manifest_path):
            manifest_path = os.path.join(table_path, manifest_path)
        if not os.path.isfile(manifest_path):
            basename = os.path.basename(manifest_path)
            alt = os.path.join(table_path, "metadata", basename)
            if os.path.isfile(alt):
                manifest_path = alt

        if not os.path.isfile(manifest_path):
            fail(f"Manifest file not found: {manifest_path}")
            continue

        # Parse manifest
        try:
            with open(manifest_path, "rb") as f:
                manifest_records = list(fastavro.reader(f))
            data_file_count = len(manifest_records)
            total_data_files += data_file_count

            if verbose:
                info(f"    Contains {data_file_count} data file entries")
                for j, entry in enumerate(manifest_records):
                    df = entry.get("data_file", entry)
                    fp = df.get("file_path", "?")
                    rc = df.get("record_count", "?")
                    info(f"      [{j}] {os.path.basename(fp)} — {rc} records")
        except Exception as e:
            fail(f"Failed to parse manifest Avro: {e}")
            continue

    ok(f"Manifest chain intact: {total_data_files} data file entries total")

    # Verify data files exist on disk
    missing_files = 0
    for ml_record in manifest_list_records:
        manifest_path = ml_record.get("manifest_path", "")
        if not os.path.isabs(manifest_path):
            manifest_path = os.path.join(table_path, manifest_path)
        if not os.path.isfile(manifest_path):
            manifest_path = os.path.join(table_path, "metadata",
                                         os.path.basename(manifest_path))

        if not os.path.isfile(manifest_path):
            continue

        with open(manifest_path, "rb") as f:
            for entry in fastavro.reader(f):
                df = entry.get("data_file", entry)
                fp = df.get("file_path", "")
                if not os.path.isabs(fp):
                    fp = os.path.join(table_path, fp)
                if not os.path.isfile(fp):
                    # Try relative to table root
                    alt = os.path.join(table_path, os.path.basename(fp))
                    if not os.path.isfile(alt):
                        missing_files += 1
                        fail(f"Data file missing: {fp}")

    if missing_files == 0:
        ok("All referenced data files exist on disk")


